const dictionaries: Record<Language, Record<MessageKey, string>> = {
  en,
  "zh-CN": zh,
};

type Values = Record<string, number | string>;
export type Translate = (key: MessageKey, values?: Values) => string;

interface I18nValue {
  language: Language;
  locale: string;
  setLanguage: (language: Language) => void;
  t: Translate;
}

const I18nContext = createContext<I18nValue | null>(null);

function initialLanguage(): Language {
  const saved = localStorage.getItem("graft-language");
  if (saved === "en" || saved === "zh-CN") return saved;
  return navigator.language.toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguage] = useState<Language>(initialLanguage);

  useEffect(() => {
    localStorage.setItem("graft-language", language);
    document.documentElement.lang = language;
  }, [language]);

  const t = useCallback<Translate>(
    (key, values) => {
      let message = dictionaries[language][key];
      for (const [name, value] of Object.entries(values ?? {})) {
        message = message.replaceAll(`{${name}}`, String(value));
      }
      return message;
    },
    [language],
  );

  const value = useMemo(
    () => ({ language, locale: language === "zh-CN" ? "zh-CN" : "en-US", setLanguage, t }),
    [language, t],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n must be used inside I18nProvider");
  return value;
}
